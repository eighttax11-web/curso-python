a = 4
b = 2

resultado = a == b  # Igual
print(f'Resultado == : {resultado}')

resultado = a != b  # Diferente
print(f'Resultado != : {resultado}')

resultado = a > b  # Mayor que
print(f'Resultado > : {resultado}')

resultado = a < b  # Menor que
print(f'Resultado < : {resultado}')

resultado = a >= b  # Mayor o igual que
print(f'Resultado >= : {resultado}')

resultado = a <= b  # Menor o igual que
print(f'Resultado <= : {resultado}')
